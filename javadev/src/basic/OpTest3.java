package basic;

public class OpTest3 {
	public static void main(String[] args) {
		System.out.println(true && true);   // 논리 AND 연산자
		System.out.println(true && false);  // 논리 AND 연산자
		System.out.println(false && false); // 논리 AND 연산자
		
		System.out.println(true || true); // 논리 OR 연산자
		System.out.println(true ||false); // 논리 OR 연산자
		System.out.println(false||false); // 논리 OR 연산자
		
		System.out.println(!true);
		System.out.println(!false);
	}
}
